package com.dontler.life.ui;

public class PaintConfig {

    private final int width;
    private final int height;

    public PaintConfig(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
